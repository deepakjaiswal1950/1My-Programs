#include <iostream>
#include <cmath>
using namespace std;
 
int main () {
   // number definition:
    double d = 200.374;
    int    i = -1000;
    float  f = 230.47;
    
   // mathematical operations;
   cout << "sin(d) :" << sin(d) << endl;
   cout << "abs(i)  :" << abs(i) << endl;
   cout << "floor(d) :" << floor(d) << endl;
   cout << "sqrt(f) :" << sqrt(f) << endl;
   cout << "pow( d, 2) :" << pow(d, 2) << endl;
 
   return 0;
}