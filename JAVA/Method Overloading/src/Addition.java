public class Addition {

    void display(int a, int b ) {

        int x = a , y = b  ;
        int total = x + y ;

        System.out.println("Addition is : " + total);
    }

    void display(int a , float b) {

        int x = a ;
        float y = b;
        float mul = x*y;

        System.out.println("Multiplication is : " + mul);
    }
}