name = "Deepak Jaiswal"
print(name)

# Function to find length of string
print(len(name))


# Function to make all strings characters into lower case order
lower = name.lower()
print(lower)


# Function to make all strings characters into upper case order
upper = name.upper()
print(upper)


# Function to replace required string or character form the string .
Rep = name.replace("Jaiswal","JAISWAL")
print(Rep)
