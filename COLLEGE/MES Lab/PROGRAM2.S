
; WRITE AN ALP TO FIND THE SUM OF FIRST TEN INTEGER NUMBERS

	AREA PROGRAM, CODE, READONLY		; PROGRAM PREREQUISITES IN READ ONLY MEMORY
	ENTRY								; BEGINNING OF PROGRAM
	
	LDR R0, = LIMIT						; LOAD ADDRESS OF "LIMIT" IN R0
	LDR R1, [R0]						; LOAD CONTENT OF "LIMIT" INTO R1 
	
LOOP ADDS R3, R3, R1					; ADD R1 WITH R3, STORE RESULT IN R3
	 MOV R2, R3							; MOVE COMPUTED ADDITION RESULT FROM R3 TO R2
	 SUB R1, #1							; DECREASE R1 VALUE BY 1
	 CMP R1, #0 						; CHECK IF "LIMIT" IS EQUAL TO 0
	 BEQ STORE							; IF LIMIT IS ZERO, BRANCH TO LABEL "STORE"
	 BNE LOOP							; BRANCH UNTIL ZERO FLAG IS SET I.E. UNTIL R1 BECOMES EQUAL TO ZERO
	
STORE LDR R3, = RESULTADDRESS			; GET THE MEMORY ADDRESS OF "RESULTADDRESS"
	  STR R2, [R3]						; STORE CONTENTS OF R2 INTO MEMORY LOCATION POINTED BY RESULTADDRESS
	
STOP B STOP								; INFINITE LOOP

LIMIT DCD 10							; SET UPPER LIMIT VALUE

	AREA WRITEDATA, DATA, READWRITE		; PROGRAM PREREQUISITES IN READ WRITE MEMORY
RESULTADDRESS DCD 0X40000000			; ADRRESS OF "RESULTADDRESS" CONTAINING SUM OF FIRST TEN INTEGERS	
	END									; END OF PROGRAM
	 